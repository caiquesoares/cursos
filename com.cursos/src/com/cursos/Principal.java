    private void btn_excluirCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirCursoActionPerformed
        int index = tbl_Cursos.getSelectedRow();
                
        if (index >= 0) {
            int id = Integer.parseInt(input_IdCurso.getText());
            CursoController.Excluir(id);
            input_IdCurso.setText("");
            input_NomeCurso.setText("");
        }
        carregarTabelaCurso();
        modo = "Navegar";
        ControleVisualizacaoCamposCurso();
    }//GEN-LAST:event_btn_excluirCursoActionPerformed

    private void btn_SalvarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarCursoActionPerformed
        int id = Integer.parseInt(input_IdCurso.getText());

        if (modo.equals("Novo")) {
            Curso c = new Curso(id, input_NomeCurso.getText());
            CursoController.Incluir(c);
        } else if (modo.equals("Editar")) {
            Curso c = new Curso(id, input_NomeCurso.getText());
            CursoController.Alterar(c);
        }

        carregarTabelaCurso();
        modo = "Navegar";
        ControleVisualizacaoCamposCurso();
        input_IdCurso.setText("");
        input_NomeCurso.setText("");
    }//GEN-LAST:event_btn_SalvarCursoActionPerformed

    private void tbl_CursosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_CursosMouseClicked
        int index = tbl_Cursos.getSelectedRow();

        if (index >= 0) {
            modo = "Selecao";
            ControleVisualizacaoCamposCurso();

            input_IdCurso.setText(String.valueOf(tbl_Cursos.getModel().getValueAt(index, 0)));
            input_NomeCurso.setText(String.valueOf(tbl_Cursos.getModel().getValueAt(index, 1)));            
        }

    }//GEN-LAST:event_tbl_CursosMouseClicked

