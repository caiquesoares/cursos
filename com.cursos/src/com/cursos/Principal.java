    public void ControleVisualizacaoCamposCurso() {
        switch (modo) {
            case "Navegar":
                btn_addCurso.setEnabled(true);
                btn_alterarCurso.setEnabled(false);
                btn_CancelarCurso.setEnabled(false);
                btn_excluirCurso.setEnabled(false);
                btn_SalvarCurso.setEnabled(false);
                input_IdCurso.setEnabled(false);
                input_NomeCurso.setEnabled(false);
                break;
            case "Novo":
                btn_addCurso.setEnabled(false);
                btn_alterarCurso.setEnabled(false);
                btn_CancelarCurso.setEnabled(true);
                btn_excluirCurso.setEnabled(false);
                btn_SalvarCurso.setEnabled(true);
                input_IdCurso.setEnabled(true);
                input_NomeCurso.setEnabled(true);
                break;
            case "Editar":
                btn_addCurso.setEnabled(false);
                btn_alterarCurso.setEnabled(false);
                btn_CancelarCurso.setEnabled(true);
                btn_excluirCurso.setEnabled(false);
                btn_SalvarCurso.setEnabled(true);
                input_IdCurso.setEnabled(true);
                input_NomeCurso.setEnabled(true);
                break;
            case "Excluir":
                btn_addCurso.setEnabled(true);
                btn_alterarCurso.setEnabled(false);
                btn_CancelarCurso.setEnabled(false);
                btn_excluirCurso.setEnabled(false);
                btn_SalvarCurso.setEnabled(false);
                input_IdCurso.setEnabled(false);
                input_NomeCurso.setEnabled(false);
                break;
            case "Selecao":
                btn_addCurso.setEnabled(true);
                btn_alterarCurso.setEnabled(true);
                btn_CancelarCurso.setEnabled(false);
                btn_excluirCurso.setEnabled(true);
                btn_SalvarCurso.setEnabled(false);
                input_IdCurso.setEnabled(false);
                input_NomeCurso.setEnabled(false);
                break;
            default:
                System.out.println("Modo Inv√°lido");
        }
    }

